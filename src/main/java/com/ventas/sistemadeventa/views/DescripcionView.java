    private void actulizarData() {
        IDescripcionDao descDao = new DescripcionDaoImpl();
        List<Descripcion> desc = descDao.listarDescripciones();
        Object data[][] = new Object[desc.size()][6];
        int index = 0;
        for (Descripcion item : desc) {
            data[index][0] = item.getId();
            data[index][1] = item.getDescripcionLarga();
            data[index][2] = item.getDescripcionCorta();
            data[index][3] = item.getFechaIngreso();
            index++;
        }

        dataTableDesc.setModel(new DescripcionGrid(data, 1, 4));
    }

