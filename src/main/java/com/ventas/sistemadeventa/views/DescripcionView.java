    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (dataTableDesc.getSelectedRow() >= 0) {
            int id = Integer.parseInt(dataTableDesc.getModel().getValueAt(dataTableDesc.getSelectedRow(), 0).toString());
            modificarDescripcion(id);
        } else {
            JOptionPane.showMessageDialog(null, "Debe Seleccionar una fila", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void dataTableDescMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dataTableDescMouseClicked
        JTable table = (JTable) evt.getSource();
        Point point = evt.getPoint();
        int row = table.rowAtPoint(point);
        if (evt.getClickCount() == 2 && table.getSelectedRow() != -1) {
            int id = Integer.parseInt(table.getModel().getValueAt(row, 0).toString());
            modificarDescripcion(id);

        }
    }//GEN-LAST:event_dataTableDescMouseClicked

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        if (dataTableDesc.getSelectedRow() >= 0) {
            int resp = JOptionPane.showConfirmDialog(null, "¿Esta seguro?", "Alerta!", JOptionPane.YES_NO_OPTION);
            System.out.println("Resp: " + resp);
            if (resp == JOptionPane.YES_OPTION) {
                int id = Integer.parseInt(dataTableDesc.getModel().getValueAt(dataTableDesc.getSelectedRow(), 0).toString());
                IDescripcionDao ux = new DescripcionDaoImpl();
                ux.borrarDescripcion(id);
                actulizarData();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe Seleccionar un usuario", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void modificarDescripcion(int id){
        IDescripcionDao descDao = new DescripcionDaoImpl();
        Descripcion desc = descDao.buscarDescripcionPorId(id);
        
        new NuevaDescripcionView(desc).setVisible(true);
    }
    
