    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (dataUsuarios.getSelectedRow() >= 0) {
            int id = Integer.parseInt(dataUsuarios.getModel().getValueAt(dataUsuarios.getSelectedRow(), 0).toString());
            modificarUsuario(id);
        } else {
            JOptionPane.showMessageDialog(null, "Debe Seleccionar un usuario", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        if (dataUsuarios.getSelectedRow() >= 0) {
            int resp = JOptionPane.showConfirmDialog(null, "¬øEsta seguro?", "Alerta!", JOptionPane.YES_NO_OPTION);
            System.out.println("Resp: " + resp);
            if (resp == JOptionPane.YES_OPTION) {
                int id = Integer.parseInt(dataUsuarios.getModel().getValueAt(dataUsuarios.getSelectedRow(), 0).toString());
                IUsuarioDao ux = new UsuarioDaoImp();
                ux.borrarUsuario(id);
                actulizarData();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe Seleccionar un usuario", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void modificarUsuario(int id) {
        IUsuarioDao usuarioDao = new UsuarioDaoImp();
        Usuario us = usuarioDao.buscarUsuarioPorId(id);
        new NuevoUsuarioView(us).setVisible(true);
    }

